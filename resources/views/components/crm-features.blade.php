<script>
    (() => {
        const tabWrap = document.querySelector('[data-tabs]');
        if (!tabWrap) return;
        const tabs = Array.from(tabWrap.querySelectorAll('[data-tab]'));
        const panels = Array.from(document.querySelectorAll('[data-tab-panel]'));
        const setActive = (index) => {
            tabs.forEach((tab, i) => {
                tab.setAttribute('aria-selected', i === index ? 'true' : 'false');
                tab.classList.toggle('text-white', i === index);
                tab.classList.toggle('text-white/70', i !== index);
                tab.classList.toggle('border-[#7b1f6a]', i === index);
                tab.classList.toggle('border-white/10', i !== index);
            });
            panels.forEach((panel) => {
                const isActive = panel.getAttribute('data-tab-panel') === String(index);
                panel.classList.toggle('hidden', !isActive);
                if (isActive) {
                    panel.classList.remove('tab-panel-animate');
                    void panel.offsetWidth;
                    panel.classList.add('tab-panel-animate');
                }
            });
        };
        tabs.forEach((tab) => {
            tab.addEventListener('click', () => setActive(Number(tab.getAttribute('data-tab'))));
        });
        setActive(0);
    })();
</script>
